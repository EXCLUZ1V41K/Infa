x = 2
x1 = -0.5
x2 = 1.5
y = 1.5
y1 = 2.3
y2 = -0.8
R = float(input("R = "))
if (x * x + y * y) < R * R:
    print("Первая точка падает на окр.")
if (x1 * x1 + y1 * y1) < R * R:
    print("Вторая точка падает на окр.")
if (x2 * x2 + y2 * y2) < R * R:
    print("Третья точка падает на окр.")
else:
    print("Точки не падают на окр.")
